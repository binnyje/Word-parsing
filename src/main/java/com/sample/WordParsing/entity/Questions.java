package com.sample.WordParsing.entity;

public class Questions {
	private String questionGroupMarker;
	private String questionMarker;
	private String answerChoiceMarker;
	public String getQuestionGroupMarker() {
		return questionGroupMarker;
	}
	public void setQuestionGroupMarker(String questionGroupMarker) {
		this.questionGroupMarker = questionGroupMarker;
	}
	public String getQuestionMarker() {
		return questionMarker;
	}
	public void setQuestionMarker(String questionMarker) {
		this.questionMarker = questionMarker;
	}
	public String getAnswerChoiceMarker() {
		return answerChoiceMarker;
	}
	public void setAnswerChoiceMarker(String answerChoiceMarker) {
		this.answerChoiceMarker = answerChoiceMarker;
	}
	
	

}
